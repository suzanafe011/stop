				Além disso, fica clara a possibilidade de reunir as diversas imagens registradas ao mesmo tempo em uma só, sempre com suporte para ajustes manuais feitos na parte de “pós-produção” da fotografia a fim de deixar a forma final a critério do usuário.</p>
