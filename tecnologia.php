		<p>Segundo descobriu o site LetsGoDigital, a ideia é uma matriz de quatro por quatro localizada onde hoje se vê apenas até quatro câmeras nos principais smartphones comerciais do mundo. Uma das ideias inclui o uso de um pequeno espelho entre as câmeras e o flash a fim de facilitar o regitro de selfies.
