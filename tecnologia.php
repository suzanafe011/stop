			A revolução dos dados chega ao comércio exterior
		</font>
		<font class="text-center"size="4" face="Georgia" color="000000">
			Primeiro banco de dados público sobre o setor exportador traz informações sobre abertura comercial.Iniciativa começa a inspirar estudos e políticas para tornar empresas e países mais competitivos.
		</font>
		
		<img src="tecnologia.jpg" class="col-6" width="300px" height="500px"/><br>
		
	<p class=" col-6">Entre as muitas revoluções tecnológicas ocorridas nos últimos 10 anos, está uma crucial para o sucesso de todas as economias, especialmente aquelas em desenvolvimento, como as da América Latina e o Caribe: a de dados sobre comércio exterior.<br>Com a crescente digitalização das informações obtidas pelas agências alfandegárias, será possível determinar como reduzir os custos de importações e exportações; e de que forma os serviços de promoção comercial podem se tornar mais eficazes.

		Foi assim que surgiu a Exporter Dynamics Database (EDD, em inglês), a primeira base de dados pública sobre os fluxos de comércio entre os países, baseada em empresas exportadoras e compilada pelo Banco Mundial. A EDD traz dados sobre empresas de 70 países — tanto os desenvolvidos quanto os em desenvolvimento —, cobrindo temas como número e tamanho, concentração e diversificação, além das dinâmicas de entrada e sobrevivência nos mercados exportadores.
		“Abertura comercial é uma condição fundamental para o crescimento econômico dos países. Mas o comércio não é realizado por países, e sim pelas empresas. A globalização mudou a cara delas e as colocou em uma posição central. Por isso, é tão importante estudá-las”, comenta a economista Ana Margarida Fernandes, que está à frente da EDD.</p>
		<p>As empresas exportadoras são mais produtivas e inovadoras, pagam salários melhores e usam mais habilidades, segundo estudos realizados com base na EDD. Ao mesmo tempo, esse é um mercado que ainda favorece as grandes. Globalmente, 1% das empresas respondem por 56% das exportações (exceto petróleo e derivados). No longo prazo, tais firmas tendem a responder por mais de 40% no crescimento das exportações.
			Enquanto isso, globalmente, só 43% dos novos exportadores sobrevivem ao primeiro ano de competição. Quanto mais pobre o país de origem da empresa, maior a possibilidade de ela fracassar.
— John Bolton (@AmbJohnBolton) 21 de novembro de 2018.</p>
<p>Embora a maior parte das informações da EDD se refira ao período entre 2005 e 2012, para alguns países há números disponíveis desde os anos 1990 até 2014. Esses dados vêm inspirando uma série de pesquisas, inclusive sobre a América Latina e o Caribe.</p>
