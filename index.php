</p><p>De acordo com a polícia, o incidente começou por volta das 21h30 (horário local) quando dois homens se confrontaram fisicamente. Um deles, de 21 anos, sacou uma arma e disparou, ferindo o outro rapaz, de 18 anos e que está em condição séria no hospital, além de uma menina de 12 anos, que também foi encaminhada para atendimento médico e estava consciente.
A polícia não sabe o motivo da briga.
