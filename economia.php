		<p>Os juros são usados pelo BC para tentar controlar a inflação. De modo geral, quando a inflação está alta, o BC sobe os juros para reduzir o consumo e forçar a queda dos preços. Quando a inflação está baixa, o BC derruba os juros para estimular o consumo.</p>
		<p>No mês passado, o Comitê de Política Monetária do BC decidiu manter a taxa de juros em 6,5% ao ano, no menor nível da história (o Copom foi criado em 1996). A metodologia utilizada é a mesma do IPCA, considerada a inflação oficial; a diferença está no período de coleta dos preços e na abrangência geográfica.</p>
