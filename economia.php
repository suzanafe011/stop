		</div>
		<p>Os preços do petróleo caem às mínimas em um ano nesta sexta-feira, 23, em meio a contínuos temores de que o mercado possa estar sofrendo com um excesso de oferta, e que a Organização de Países Exportadores de Petróleo (Opep) tenha apenas poucas opções para investir contra a esperada expansão no suprimento vindo dos Estados Unidos.</p><p>
Às 10h04 (de Brasília), o barril do WTI para janeiro de 2019 tinha baixa de 4,21%, a US$ 52,33, na New York Mercantile Exchange (Nymex), enquanto o Brent par igual mês caía 2,51%, a US$ 61,03 o barril, na Intercontinental Exchange (ICE), em Londres.</p>
<p>Se a Opep mantiver o atual nível de oferta, o excesso de petróleo pesaria sobre preços e tiraria cerca de 500 mil barris por dia da produção americana no ano que vem, dizem os analistas.
No entanto, se o cartel cortar sua produção em 2019 em cerca de 1,3 milhão de barris por dia e mantiver a oferta nesse mesmo nível em 2020, a produção dos EUA pode subir em cerca de 1 milhão de barris por dia em 2019 e 2020.
